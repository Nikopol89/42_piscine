#include <unistd.h>

void	ft_is_negative(int n)
{
	char a;
	char b;

	a = 'P';
	b = 'N';
	if (n >= 0)
	{
		write(1, &a, 1);
	}
	if (n < 0)
	{
		write(1, &b, 1);
	}
}

int		main(void)
{
	ft_is_negative(0);
}
