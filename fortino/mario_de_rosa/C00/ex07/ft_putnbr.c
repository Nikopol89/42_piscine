#include <unistd.h>

void	ft_putnbr(int n)
{
	int		nu;
	char	st;

	if (n == -2147483648)
		write(1, &"-2147483648", 11);
	else if (n < 0)
	{
		nu = (-1 * n);
		write(1, "-", 1);
		ft_putnbr(nu);
	}
	else
	{
		if (n < 10)
		{
			st = n + '0';
			write(1, &st, 1);
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
	}
}

int main(void)
{
	ft_putnbr(2147483647);
}