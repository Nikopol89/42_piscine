#include <stdio.h>

int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			i++;
		else
			return (0);
	}
	return (1);
}

int		main(void)
{
	char str[] = "at0ani";
	int output;

	output = ft_str_is_lowercase(str);
	printf("%d", output);
}
